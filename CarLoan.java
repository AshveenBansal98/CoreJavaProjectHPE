    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
      String Principal = (String) jTextField1.getText(); 
        int pr = Integer.parseInt(Principal);
        int time = 0;
        if(jComboBox1.getSelectedIndex() == 1){
            time = 60;}
        if(jComboBox1.getSelectedIndex() == 2){
            time = 84;}
        double r = 9.25/1200;
        double rx = Math.pow(r+1, time);
        double emi = Math.round( pr*r*rx/(rx-1)*100 );
        emi = emi/100;
        if(jComboBox1.getSelectedIndex()!=0){
        jTextField2.setText("Rs." + emi);
        double interest = Math.round( (emi*time - pr)*100 );
        interest = interest/100;
        jTextField3.setText("Rs." + interest); 
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

